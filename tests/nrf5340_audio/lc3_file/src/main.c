int sd_card_read_header_fake(char *buf, size_t *size, struct fs_file_t *f_seg_read_entry)
{
	uint8_t lc3_headerarray[] = {
		0x1c, 0xcc, // file_id: 0xcc1c
		0x12, 0x00, // hdr_size: 0x0012
		0x40, 0x01, // sample_rate: 0x0140
		0xe0, 0x01, // bit_rate: 0x01e0
		0x01, 0x00, // channels: 0x0001
		0xe8, 0x03, // frame_duration: 0x03e8
		0x00, 0x00, // rfu: 0x0000
		0x6c, 0x00, // signal_len_lsb: 0x006c
		0x42, 0x00  // signal_len_msb: 0x0042
	};

	zassert_equal(*size, sizeof(lc3_headerarray), "Expect header size to be requested");

	memcpy(buf, lc3_headerarray, sizeof(lc3_headerarray));

	return 0;
}

